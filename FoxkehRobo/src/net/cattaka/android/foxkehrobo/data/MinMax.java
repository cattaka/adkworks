
package net.cattaka.android.foxkehrobo.data;

public class MinMax {
    public final int yawMin;

    public final int yawMax;

    public final int pitchMin;

    public final int pitchMax;

    public MinMax(int yawMin, int yawmax, int pitchMin, int pitchMax) {
        super();
        this.yawMin = yawMin;
        this.yawMax = yawmax;
        this.pitchMin = pitchMin;
        this.pitchMax = pitchMax;
    }

}
